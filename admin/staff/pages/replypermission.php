        $status = $_POST['status'];
        
        $comment = $_POST['comment'];
        
        $error ="";
        //check if empty
        if(!empty($comment) && !empty($staff_id) && !empty($accepted_date) && !empty($status)){
            
            $checkobject = new checks();
            
            $comment_checks = $checkobject->inputs($comment);
                    
            $staff_id_checks = $checkobject->inputs($staff_id);
            
            $accepted_date_checks = $checkobject->inputs($accepted_date);
            
            $status_checks = $checkobject->inputs($status);
            
            if($comment_checks && $status_checks && $staff_id_checks && $accepted_date_checks){
                
                //calling the permission class
                
                $permission =  new permission();
                
                if($permission ->user_exists($staff_id)){
                    
                    if($permission->reply($staff_id,$status,$accepted_date,$comment)){
                        $error = "<span class='alert alert-success'>Reply Successfully Sent </span>";
                    }else{
                        
                        $error = "<span class='alert alert-danger'> Sorry Could not Send Reply. Contact Admin</span>";
                    }
                    
                }else{
                    $error = "<span class='alert alert-danger'> User Not Registered </span>";
                }
            }else{
                return false;
            }
        }else{
            $error = "<span class='alert alert-danger'> All Fields Required</span>";
        }
